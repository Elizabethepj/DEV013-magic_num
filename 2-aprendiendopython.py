"""Aprendiendo python."""
nombre_curso = "Python curso"

print(nombre_curso)
print(len(nombre_curso))
# accede al índice 2
print(nombre_curso[2])
# accede al índice 0 hasta el índice 5
print(nombre_curso[0:5])
# accede al valor desde el índice 1 hasta el final
print(nombre_curso[1:])
# accede desde el índice cero hast el indicado, en este caso, 1
print(nombre_curso[:1])
# accede a todo el string
print(nombre_curso[:])
